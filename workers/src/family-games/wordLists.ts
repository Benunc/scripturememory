// Get all words as a flat array for easy access
export function getAllDistractorWords(): string[] {
  const allWords: string[] = [];
  
  // Add all Bible words
  Object.values(bibleWords).forEach(category => {
    allWords.push(...category);
  });
  
  // Add silly words
  allWords.push(...sillyWords);
  
  return allWords;
}

// Get words by type (for better distractor matching)
export function getWordsByType(type: string): string[] {
  const typeLower = type.toLowerCase();
  
  if (typeLower === 'noun' || typeLower === 'nouns') {
    return [...bibleWords.nouns];
  } else if (typeLower === 'verb' || typeLower === 'verbs') {
    return [...bibleWords.verbs];
  } else if (typeLower === 'adjective' || typeLower === 'adjectives') {
    return [...bibleWords.adjectives];
  } else if (typeLower === 'adverb' || typeLower === 'adverbs') {
    return [...bibleWords.adverbs];
  }
  
  // Return all words if type doesn't match
  return getAllDistractorWords();
}

